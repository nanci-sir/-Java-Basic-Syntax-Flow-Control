package com.nanci.loop;

public class whileDemo1 {
    public static void main(String[] args) {
        int i =0;
        while(i<3){
            System.out.println("nanci sir");
            i++;
        }
        System.out.println("------------");
        int j =0;
        while(j<3){
            System.out.println("nanci sir");//死循环
        }
    }
}
